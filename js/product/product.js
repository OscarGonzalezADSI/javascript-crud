class Product
{
    
    setNombre(nombre)
    {
        this.nombre = nombre;
    }
    
    getNombre()
    {
        return this.nombre;
    }
    
    setPrecio(precio)
    {
        this.precio = precio;
    }
    
    getPrecio()
    {
        return this.precio;
    }
    
    setCantidad(cantidad)
    {
        this.cantidad = cantidad;
    }
    
    getCantidad()
    {
        return this.cantidad;
    }
}
